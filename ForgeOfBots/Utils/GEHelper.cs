         return false;
      }
      public static IEnumerable<T> GetTroopRow<T>(this T[,] array, int rowIndex)
      {
         int columnsCount = array.GetLength(1);
         for (int colIndex = 0; colIndex < columnsCount; colIndex++)
            yield return array[rowIndex, colIndex];
      }
   }

